export function translateGraphTerm(value: string, locale: Locale): string {
  return graphTermMap[value]?.[locale] ?? value;
}

export function formatTopEventTypes(eventTypes: string[], locale: Locale): string {
  if (eventTypes.length === 0) {
    return locale === "zh" ? "暂无" : "None";
  }
  return eventTypes.map((item) => translateGraphTerm(item, locale)).join(", ");
}

